# Task :: z-hair
# Little Z decided to cut his long hair. He has scissors that can cut K consecutive hairs, and he can make at most M cuts.
#
# He wants to make at most M cuts. He wants to minimize the length of the longest hair.
#
# INPUT:
# From the first line of the standard input read three integers N, K and M, where (1 <= N, K, M <= 2000). From the next N lines read N integers representing the length of each individual hair. Each length will be in the interval [0, 1000000]
#
# OUTPUT:
# To the standard output write one integer representing the minimal length of the longest hair after he has cut his hair
#
# Input:
# 5 1 2
# 1
# 2
# 3
# 4
# 5
#
# Output:
# 3
#
# Input:
# 5 3 2
# 1
# 2
# 3
# 4
# 5
#
# Output:
# 0
